function generateRandomEmo() {
	let length = (2 + (Math.floor(Math.random() * 4))) * 32;

	let emoString = "";
	let i = 0;

	emoArray = getEmojiArray();

	while (i < length) {
		let random = Math.floor(Math.random() * 255);
		emoString = emoString + emoArray[random];
		++i;
	}

	return emoString;
}

function checkInputString (inputString) {

	if (inputString == "") {
		console.log("Input is empty");
		return false;
	}

	if (inputString.slice(0,3) === "0x:") {
		console.log("Input is encoded Hex");
		return true;
	} 

	if(inputString.slice(0,4) === "b64:") {
		console.log("Input is encoded B64");
		return true;
	}

	let emojiArray = getEmojiArray();
	let emojiString = inputString;
	let indexArray = [];
	let string = "";

	let j = 0;
	

	emojiString.split('​').forEach(function (c) {
		string = c;
		if (string) {
			for (let i = 0; i < emojiArray.length; i++) {
				if (string === emojiArray[i] ) {
					indexArray[j] = i;
				}
			}
			j++;
		}
	});

	//counts how many times the emojis were in the base array
	let true_count = 0;
	let index;

	for (let i = 0; i < indexArray.length; i++) {
		
		index = indexArray[i];
		if (index < 256 && index > 0) {
			true_count++;
		}
	}

	if (true_count > 20) {
		console.log("Input is Encoded Emoji");
		return true
	}
	return false;

}

